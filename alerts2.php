<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Recently, members of the public have received phishing SMS messages, 
	claiming that the recipients’ loan application has been approved, attached with 
	a telephone number for follow-up. The public should stay alert to the phishing 
	SMS messages and fraud activities related to money lending. Please refer to the 
	website of The Investor and Financial Education Council (IFEC) to understand 
	how best to safeguard your own interests and prevent from falling prey to money 
	lending traps by unscrupulous financial intermediaries.
	</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>Do not believe any unknown calls or SMS messages. If the callers claim that they work for or represent a bank, finance company and even the government, contact corresponding offices to verify their identities;</li>
	<li>Banks or mortgage companies will never request their clients to borrow money from another finance company to repay existing loans;</li>
	<li>You should only borrow from banks and reputable finance companies;</li>
	<li>Collect the loan by cheque or bank transfer, not by cash, and keep the record;</li>
	<li>Read the content and terms of the contract carefully before signing;</li>
	</ul>
