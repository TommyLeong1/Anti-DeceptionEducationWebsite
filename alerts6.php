<img src="AlertsVideo_photo/21.png" height="280">
<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Having seen a recruitment post on social media, a member of the public contacted 
	the post author for job application and was asked to set up a virtual bank account 
	for receiving salary. The unwary victim complied and sent the account details and 
	screenshot of identity card to the post author. On the same day, the victim received 
	a message purporting to be from bank staff claiming that he was granted a pre-approved
	 credit card and personal loan of tens of thousands dollars. The victim rejected 
	 but then was asked for his account password for cancellation of the loan. Not long 
	 after disclosing his password, the victim received the loan through his account. 
	 The scammer asked the victim to transfer the loan to a personal local bank account 
	 for repayment. After the transfer, the victim approached the genuine bank staff for 
	 verification and realized that he had fallen into a scam in which the scammer applied 
	 for loan using his identity and defrauded the money.
	</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>Be extremely cautious if post authors do not disclose their company information in recruitment advertisements, or just leave their social media or instant messaging application accounts for contact;</li>
	<li>Learn about the company’s background and business nature if company information is provided in recruitment messages;</li>
	<li>If the callers or senders claim themselves as bank staff, contact the corresponding banks to verify the identity.  You may also check out the Hong Kong Association of Banks’ website to verify phone calls purported to be from banks;</li>
	<li>Remind your relatives and friends to stay vigilant against deception;</li>
	<li>If in doubt, please call the “Anti-Scam Helpline 18222” for enquiries.</li>
	</ul>
