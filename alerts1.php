<h1><font color="red" size="+2">Beware of Phishing SMS Messages Purportedly from Transport Department (HKeToll)</font></h1>
<br>
<img src="AlertsVideo_photo/9.png" height="280">
<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Recently, members of the public have received phishing SMS messages purporting to 
	be from the Transport Department (HKeToll) which claims that the recipient’s automatic 
	payment failed. Members of the public are lured into clicking on the embedded link which 
	will direct them to a fraudulent HKeToll website where they are asked to input their credit 
	card information for payment. The Transport Department has issued a press release for clarification.
	<br><br>
	Starting from early 2023, HKeToll, a free-flow tolling service, has been gradually implemented at all government tolled tunnels. The public can visit the HKeToll website for details on opening an account and payment means. A customer service hotline 3853 7333 is set up to answer public enquiries from 9am to 9pm daily. 
	</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>Do not connect to any suspicious websites or download any attachments by clicking on hyperlinks embedded in SMS messages, emails or websites;</li>
	<li>Do not input your personal information, credit card details, 3-digit security codes and one-time password into unknown websites or applications;</li>
	<li>Enter the suspicious URL on “Scameter” or “Scameter+”, the mobile application of “Scameter”, for security check in addition to seeking verification from official organizations;</li>
	<li>Remind your relatives and friends to stay vigilant against deception;</li>
	<li>If in doubt, please call the “Anti-Scam Helpline 18222” for enquiries.</li>
	</ul>
