<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Recently, scammers impersonating officers of the ADCC sent messages to members 
	of the public via the instant messaging application “LINE” with the name “ADCC”, 
	seeking to obtain the recipients’ personal information, bank account details and 
	passwords by deception.
	</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>ADCC will not forward your call to any law enforcement officers of other authorities;</li>
	<li>Even if the callers are able to tell your personal information or send you legal documents with your photo, it does not necessarily mean that they are genuine law enforcement officers. Scammers can obtain the personal information of the public by unlawful means;</li>
	<li>If the callers claim themselves as officers of law enforcement agencies or government organizations, stay alert and contact corresponding offices to verify their identities;</li>
	<li>Do not disclose your personal information, bank account number or password to strangers;</li>
	<li>If in doubt, please call the “Anti-Scam Helpline 18222” for enquiries.</li>
