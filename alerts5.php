<h1><font color="red" size="+2">Beware of Scam Calls Purportedly from CA or OFCA (Real-name Registration for SIM Cards) </font></h1>
<br>
<img src="AlertsVideo_photo/20.png" height="280">
<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Recently, there are scammers impersonating staff from the Communications Authority (CA) 
	or the Office of the Communications Authority (OFCA) and calling the public. 
	The calls were made in the form of pre-recorded message or by a real person 
	claiming that as the recipients had failed to complete real-name registrations 
	for their SIM cards, their telephone services would be suspended unless they 
	provided personal information for the callers to complete the real-name registration 
	for them.
	<br><br>
	The OFCA clarified that it would not make telephone calls to members of the public 
	asking for personal data for completion of real-name registration for SIM cards. 
	Users of PPS cards which have been deactivated for not having completed their 
	real-name registration by the deadline can reactivate the service according 
	to the following.
	<br><br>
	<ul align="left">
	<li>Complete registration via an electronic registration platform by following the instructions in the final SMS notification reminder sent by their telecommunications service providers just before the registration deadline under the name “SIMREG”, provided that their PPS cards are not yet expired and have remaining value;</li>
	<li>Complete the relevant procedures at the retail outlets of their telecommunications service providers;</li>
	<li>Visit the real-name registration service counters set up by the Government at 18 designated post offices for assistance to register PPS cards for free;</li>
	<li>Please refer to the OFCA notice on Real-name Registration for SIM Cards for details.</li>
	</ul>
</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>If the callers claim themselves as staff of the CA, OFCA or telecommunications service providers, contact the corresponding offices to verify the identity.  </li>
	<li>Please refer the following link for the web links and contact information for real-name registration of PPS cards by telecommunications services providers:  https://www.ofca.gov.hk/en/consumer_focus/guide/hot_topics/sim_registration/reference_guide/index.html
</li>
	<li>Users are not required to pay or provide bank account information to complete real-name registration;</li>
	<li>Remind your relatives and friends to stay vigilant against deception;</li>
	<li>If in doubt, please call the “Anti-Scam Helpline 18222” for enquiries.</li>
	</ul>
