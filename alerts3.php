<h1><font color="red" size="+2">Beware of Phishing SMS Messages Purportedly from Water Supplies Department</font></h1>
<br>
<img src="AlertsVideo_photo/18.png" height="280">
<h1><font color="red" size="+1">Defrauding Tricks</h1></font>
	<p align="left">
	Recently, members of the public have received phishing SMS messages purporting 
	to be from the Water Supplies Department (WSD) about overcharge of water charges. 
	Members of the public are lured into clicking on the embedded link which will direct 
	them to a fraudulent WSD website where they are asked to provide their personal 
	information or credit card details for refund.
	</p>
<h1><font color="red" size="+1">Our Advice</h1></font>
	<ul align="left">
	<li>Do not connect to any suspicious websites or download any attachments by clicking on hyperlinks embedded in SMS messages, emails or websites;</li>
	<li>Do not tell anyone your personal information, credit card details, 3-digit security codes and one-time password, or input them to unknown applications or websites;</li>
	<li>Enter the suspicious URL on “Scameter” or “Scameter+”, the mobile application of “Scameter” for security check in addition to seeking verification from official organizations;</li>
	<li>Remind your relatives and friends to stay vigilant against deception;</li>
	<li>If in doubt, please call the “Anti-Scam Helpline 18222” for enquiries.</li>
	</ul>
